    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        if (row == -1) return;

        int id = Integer.parseInt(jTable1.getValueAt(row, 0).toString());

        // Ambil produk lengkap dari database
        Produk p = getProdukById(id);

        if (p != null) {
            // simpan data produk agar tombol Edit & Hapus bisa dipakai
            Px = p;
            jButton2.setEnabled(true);  // Hapus
            jButton3.setEnabled(true);  // Edit

            // buka detail
            DetailProduk dp = new DetailProduk(p);
            dp.setLocationRelativeTo(null);
            dp.setVisible(true);
        }

    }//GEN-LAST:event_jTable1MouseClicked

