    public Produk getProdukById(int id) {
        try {
            Connection K = Koneksi.Go();
            Statement S = K.createStatement();
            ResultSet R = S.executeQuery("SELECT * FROM produk WHERE id_produk=" + id);

            if (R.next()) {
                Produk p = new Produk();
                p.setId_produk(R.getInt("id_produk"));
                p.setNama_produk(R.getString("nama_produk"));
                p.setKategori(R.getString("kategori"));
                p.setStok(R.getInt("stok"));
                p.setHarga(R.getDouble("harga"));
                p.setGambar_produk(R.getString("gambar_produk"));
                p.setId_supplier(R.getInt("id_supplier"));
                return p;
        }

    } catch (Exception e) { e.printStackTrace(); }
    return null;
}


