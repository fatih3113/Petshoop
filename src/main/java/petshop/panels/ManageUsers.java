    public static void RefreshData(){
        try {
            jTable1.setRowHeight(30); 
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            
            for (int i = model.getRowCount()-1; i >=0; i--) {
                model.removeRow(i); 
            }
            
            //kode kita
            Connection K = Koneksi.Go();
            Statement S = K.createStatement();
            String Q = "SELECT * FROM pegawai";
            ResultSet R = S.executeQuery(Q);
            while (R.next()) {                 
                int ID = R.getInt("id_pegawai");
                String NAMA = R.getString("nama_pegawai");
                String JABATAN = R.getString("jabatan");
                String USERNAME = R.getString("username");
                String PASSWORD = R.getString("password_hash");
                Object[] datausers = {ID,NAMA,JABATAN,USERNAME,PASSWORD};
                model.addRow(datausers); 
            }
            
        } catch (Exception e) {
            //error handling
        }
    }

    private void SearchData() {
            // Reset tombol saat search berubah
        jButton2.setEnabled(false);
        jButton3.setEnabled(false);
        Px = null;

        String keyword = jTextField1.getText();

        try {
            jTable1.setRowHeight(30);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            Connection K = Koneksi.Go();
            Statement S = K.createStatement();

            String sql = "SELECT * FROM pegawai WHERE " +
                         "id_pegawai LIKE '%"+keyword+"%' OR " +
                         "nama_pegawai LIKE '%"+keyword+"%' OR " +
                         "jabatan LIKE '%"+keyword+"%' OR " +
                         "username LIKE '%"+keyword+"%'";

            ResultSet R = S.executeQuery(sql);

            while (R.next()) {
                model.addRow(new Object[]{
                    R.getInt("id_pegawai"),
                    R.getString("nama_pegawai"),
                    R.getString("jabatan"),
                    R.getString("username"),
                    R.getString("password_hash")
                });
            }

            R.close();
            S.close();
            K.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }


