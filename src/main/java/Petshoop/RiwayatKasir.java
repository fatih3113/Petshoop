    private void loadKasir() {
        try {
            Connection con = Koneksi.Go();
            String sql = """
                SELECT DISTINCT p.nama_pegawai
                FROM detail_transaksi d
                JOIN pegawai p ON d.id_pegawai = p.id_pegawai
            """;

            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            NamaKasir.removeAllItems();
            NamaKasir.addItem("Pilih Kasir");

            while (rs.next()) {
                NamaKasir.addItem(rs.getString("nama_pegawai"));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
    }
}



