    private void tampilkanRiwayatPerKasir() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        String namaKasir = NamaKasir.getSelectedItem().toString();
        if (namaKasir.equals("Pilih Kasir")) return;

        try {
            Connection con = Koneksi.Go();
            String sql = """
                SELECT 
                    p.nama_pegawai,
                    COUNT(DISTINCT d.no_transaksi) AS jumlah_transaksi,
                    SUM(d.total_harga) AS total_pendapatan
                FROM detail_transaksi d
                JOIN pegawai p ON d.id_pegawai = p.id_pegawai
                WHERE p.nama_pegawai = ?
                GROUP BY p.nama_pegawai
            """;

            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, namaKasir);

            ResultSet rs = ps.executeQuery();
            int no = 1;

            while (rs.next()) {
                model.addRow(new Object[]{
                    no++,
                    rs.getString("nama_pegawai"),
                    rs.getInt("jumlah_transaksi"),
                    rs.getInt("total_pendapatan")
                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            e.printStackTrace();
        }
    }
