        private void tampilDataNota() {
            DefaultTableModel model = (DefaultTableModel) tblNota.getModel();
            model.setRowCount(0);

            try {
                Connection con = Koneksi.Go();
                String sql = """
                    SELECT p.id_produk, p.nama_produk,
                           d.jumlah, d.harga_satuan, d.total_harga
                    FROM detail_transaksi d
                    JOIN produk p ON d.id_produk = p.id_produk
                    WHERE d.no_transaksi = ?
                """;

                PreparedStatement ps = con.prepareStatement(sql);
                ps.setInt(1, noTransaksi); // ðŸ”¥ FIX UTAMA
                ResultSet rs = ps.executeQuery();

                while (rs.next()) {
                    model.addRow(new Object[]{
                        rs.getInt("id_produk"),
                        rs.getString("nama_produk"),
                        rs.getInt("jumlah"),
                        rs.getInt("harga_satuan"),
                        rs.getInt("total_harga")
                    });
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }

        
    
        private void tampilTotal() {
        try {
            Connection con = Koneksi.Go();
            String sql = "SELECT total_harga, bayar, kembalian FROM kasir WHERE no_transaksi = ?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setInt(1, noTransaksi);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                txtTotalBayar.setText("Rp. " + rs.getInt("total_harga"));
                txtBayar.setText("Rp. " + rs.getInt("bayar"));
                txtKembalian.setText("Rp. " + rs.getInt("kembalian"));
                txtTotalBelanja.setText("Rp. " + rs.getInt("total_harga"));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
}
    

